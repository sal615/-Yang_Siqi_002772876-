package Model;

/**
 *
 * @author siqiyang
 */
public class Address {
    String street;
    String aptNo;
    String stateorcity;
    String zipcode;
    
    
    public Address(){

    }

    public String getStreet() {
        return street;
    }

    public void setStreet(String street) {
        this.street = street;
    }

    public String getAptNo() {
        return aptNo;
    }

    public void setAptNo(String aptNo) {
        this.aptNo = aptNo;
    }

    public String getStateorcity() {
        return stateorcity;
    }

    public void setStateorcity(String stateorcity) {
        this.stateorcity = stateorcity;
    }

    public String getZipcode() {
        return zipcode;
    }

    public void setZipcode(String zipcode) {
        this.zipcode = zipcode;
    }
    
    
    
}
